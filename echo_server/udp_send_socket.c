void
benchmark(struct udp_pcb *pcb, const ip_addr_t *addr, u16_t port)
{
    err_t err;
    struct pbuf *pkt;

    while (bytes < 147200000) {
        pkt = pbuf_alloc(PBUF_TRANSPORT, 1472, PBUF_RAM);
        if (!pkt) {
            return;
        }
        //pbuf_take(pkt, lwiperf_txbuf_const, 1472);
        memcpy(pkt->payload, lwiperf_txbuf_const, 1472);
        err = udp_sendto(pcb, pkt, addr, port);
        if (err) {
            print("Failed to send UDP packet through socket: ");
            print(lwip_strerr(err));
            putC('\n');
            pbuf_free(pkt);
            return;
        }

        pbuf_free(pkt);
        bytes += 1472;
    }

    return;
}

void continue_benchmark(void) {
    if (started) {
        benchmark(_pcb, &_addr, _port);
    }

    if (bytes >= 147200000) {
        print("Measurement stopping...\n");

        sel4cp_ppcall(1, sel4cp_msginfo_new(0, 0));
        uint64_t time_end = seL4_GetMR(0);

        print("Time differemce: ");
        puthex64(time_end - time_start);
        print(" bytes: ");
        puthex64(bytes);
        print(" driver processed: ");
        puthex64(log->transmit_bytes);
        putC('\n');
        started = false;
        bytes = 0;
    }
}

static void udp_recv_callback(void *arg, struct udp_pcb *pcb, struct pbuf *p, const ip_addr_t *addr, u16_t port)
{
    if (!started) {
        print(sel4cp_name);
        print(" measurement starting... \n");
        log->transmit_bytes = 0;
        bytes = 0;
        started = true;
        memcpy((void *)&_addr, (void *)addr, sizeof(ip_addr_t));
        _port = port;
        _pcb  = pcb;

        sel4cp_ppcall(1, sel4cp_msginfo_new(0, 0));
        time_start = seL4_GetMR(0);

        benchmark(_pcb, &_addr, _port);
    }

    pbuf_free(p);
}

int setup_socket(void)
{
    udp_socket = udp_new_ip_type(IPADDR_TYPE_V4);
    if (udp_socket == NULL) {
        print("Failed to open a UDP socket");
        return -1;
    }

    int error = udp_bind(udp_socket, IP_ANY_TYPE, SEND_PORT);
    if (error == ERR_OK) {
        udp_recv(udp_socket, udp_recv_callback, udp_socket);
    } else {
        print("Failed to bind the UDP socket");
        return -1;
    }

    return 0;
}
